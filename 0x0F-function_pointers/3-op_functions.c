#include "3-calc.h"
/**
 * op_add - Function that add two numbers
 * @a: The first number
 * @b: The second number
 * Return: sam of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function thatb subtracts two numbers
 * @a: The fisrt number
 * @b: The second number
 * Return: The result of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that multiplies two numbers
 * @a: The first number
 * @b: The second number
 * Return: product of a + b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Fuction that divides two numbers
 * @a: The first number
 * @b: The second number
 * Return: The value of a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Fuction that divides two numbers and return the remainder
 * @a: The first number
 * @b: The second number
 * Return: The remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

